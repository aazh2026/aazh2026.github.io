#!/usr/bin/env node
// Pre-build link checker: verifies all internal <a href> and <img src> in
// HTML/Markdown source files resolve to an existing Jekyll permalink, a
// local file, or a known generated path.
//
// Catches:
// - Series index pages linking to slugs that don't exist
// - 404.html hardcoded old date-style URLs
// - Missing static assets (favicon, feed, etc.)
// - Wrong file extensions (.png/.svg/.jpg)
//
// Skips:
// - External URLs (http://, https://, //)
// - Liquid template variables ({{...}}, {%...%})
// - Jekyll site variables (site., page., post.)
//
// Exits 0 on success, 1 on any broken link.

const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const SKIP_DIRS = new Set(['_site', '.git', 'node_modules', 'vendor']);

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (SKIP_DIRS.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, files);
    else if (entry.name.endsWith('.html') || entry.name.endsWith('.md')) {
      files.push(full);
    }
  }
  return files;
}

function buildPermalinkMap() {
  const set = new Set();

  // Root files
  for (const f of fs.readdirSync(ROOT)) {
    if (SKIP_DIRS.has(f)) continue;
    try {
      if ((f.endsWith('.html') || f.endsWith('.md')) && fs.statSync(f).isFile()) {
        const base = f.replace(/\.(html|md)$/, '');
        set.add('/' + base);
        set.add('/' + base + '/');
      }
    } catch {}
  }

  // _posts
  const postsDir = path.join(ROOT, '_posts');
  if (fs.existsSync(postsDir)) {
    for (const f of fs.readdirSync(postsDir)) {
      if (!f.endsWith('.md')) continue;
      const txt = fs.readFileSync(path.join(postsDir, f), 'utf8');
      const fmMatch = txt.match(/^---\n([\s\S]*?)\n---/m);
      if (!fmMatch) continue;
      const fm = fmMatch[1];
      const permalinkMatch = fm.match(/^permalink:\s*(.+?)\s*$/m);
      const slug = f.replace(/^\d{4}-\d{2}-\d{2}-/, '').replace(/\.md$/, '');
      if (permalinkMatch) {
        let p = permalinkMatch[1].trim().replace(/^["']|["']$/g, '');
        if (p.includes('{{') || p.includes('{%')) {
          set.add('/' + slug);
          set.add('/' + slug + '/');
        } else {
          p = p.replace(/\/+$/, '').replace(/\/+/g, '/');
          if (!p.startsWith('/')) p = '/' + p;
          set.add(p);
          set.add(p + '/');
        }
      } else {
        set.add('/' + slug);
        set.add('/' + slug + '/');
      }
    }
  }

  // Generated by Jekyll plugins
  for (const p of ['/feed.xml', '/sitemap.xml', '/archive/', '/tags/', '/subscribe/', '/favicon.png']) {
    set.add(p);
  }
  return set;
}

function fileExists(url) {
  const candidates = [
    path.join(ROOT, url),
    path.join(ROOT, url + '.html'),
    path.join(ROOT, url + '/index.html'),
    path.join(ROOT, url + '.png'),
    path.join(ROOT, url + '.svg'),
    path.join(ROOT, url + '.jpg'),
    path.join(ROOT, url + '.xml'),
  ];
  return candidates.some(c => fs.existsSync(c));
}

function main() {
  const htmlFiles = walk(ROOT);
  const permalinks = buildPermalinkMap();
  const linkRe = /(?:href|src)=["']([^"']+)["']/g;

  const allLinks = [];
  for (const f of htmlFiles) {
    const txt = fs.readFileSync(f, 'utf8');
    let m;
    while ((m = linkRe.exec(txt)) !== null) {
      allLinks.push({ file: f.replace(process.cwd() + '/', ''), url: m[1] });
    }
  }

  const internal = allLinks.filter(l =>
    l.url.startsWith('/') &&
    !l.url.startsWith('//') &&
    !l.url.includes('{{') &&
    !l.url.includes('{%') &&
    !l.url.includes('site.') &&
    !l.url.includes('page.')
  );

  const broken = [];
  for (const l of internal) {
    let p = l.url.split('#')[0].split('?')[0];
    if (!p) continue;
    p = p.replace(/\/+$/, '');
    if (!p) continue;
    if (!permalinks.has(p) && !permalinks.has(p + '/') && !fileExists(p)) {
      broken.push(l);
    }
  }

  console.log(`HTML files scanned: ${htmlFiles.length}`);
  console.log(`Internal links: ${internal.length}`);
  console.log(`Broken: ${broken.length}`);
  if (broken.length) {
    console.log('');
    for (const b of broken) {
      console.log(`  ${b.file} :: ${b.url}`);
    }
    process.exit(1);
  } else {
    console.log('✓ All internal links resolve');
    process.exit(0);
  }
}

main();
