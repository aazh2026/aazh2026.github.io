#!/usr/bin/env node
// Pre-build link checker: verifies all internal <a href> and <img src> in
// HTML/Markdown source files resolve to an existing Jekyll permalink, a
// local file, or a known generated path.
//
// Catches:
// - Series index pages linking to slugs that don't exist
// - 404.html hardcoded old date-style URLs
// - Missing static assets (favicon, feed, etc.)
// - Wrong file extensions (.png/.svg/.jpg)
//
// Skips:
// - External URLs (http://, https://, //)
// - Liquid template variables ({{...}}, {%...%})
// - Jekyll site variables (site., page., post.)
//
// Exits 0 on success, 1 on any broken link.

const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const SKIP_DIRS = new Set(['_site', '.git', 'node_modules', 'vendor']);

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (SKIP_DIRS.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, files);
    else if (entry.name.endsWith('.html') || entry.name.endsWith('.md')) {
      files.push(full);
    }
  }
  return files;
}

// Resolve a single .md/.html source file's served permalink(s). Reads the
// frontmatter `permalink:` if present (case-sensitive, so WRITING-GUIDE.md
// with `permalink: /writing-guide/` resolves correctly), otherwise falls
// back to the file's basename slug. Also picks up `redirect_from:` entries
// (Jekyll's `jekyll-redirect-from` plugin serves these URLs as redirects,
// so links to them are valid).
function resolvePermalinks(filePath, slugOverride) {
  const slug = slugOverride !== undefined
    ? slugOverride
    : filePath.replace(/^.*\//, '').replace(/\.(html|md)$/, '');
  const set = new Set();
  const addPath = (raw) => {
    let p = raw.trim().replace(/^["']|["']$/g, '');
    if (!p || p.includes('{{') || p.includes('{%')) return;
    p = p.replace(/\/+$/, '').replace(/\/+/g, '/');
    if (!p.startsWith('/')) p = '/' + p;
    set.add(p);
    set.add(p + '/');
  };
  try {
    const txt = fs.readFileSync(filePath, 'utf8');
    const fmMatch = txt.match(/^---\n([\s\S]*?)\n---/m);
    if (fmMatch) {
      const fm = fmMatch[1];
      const permalinkMatch = fm.match(/^permalink:\s*(.+?)\s*$/m);
      if (permalinkMatch) {
        addPath(permalinkMatch[1]);
      } else {
        set.add('/' + slug);
        set.add('/' + slug + '/');
      }
      // redirect_from: array of URLs that this page also serves (as redirects)
      // via the jekyll-redirect-from plugin. Treat them as valid link targets.
      const rfMatch = fm.match(/^redirect_from:\s*\n((?:\s+-\s+.+\n?)+)/m);
      if (rfMatch) {
        const lines = rfMatch[1].split('\n');
        for (const line of lines) {
          const m = line.match(/-\s+["']?([^"']+)["']?/);
          if (m) addPath(m[1]);
        }
      }
      return set;
    }
  } catch {}
  set.add('/' + slug);
  set.add('/' + slug + '/');
  return set;
}

function buildPermalinkMap() {
  const set = new Set();

  // Root files (about.md, WRITING-GUIDE.md, aise-series.md redirect stub, etc.)
  for (const f of fs.readdirSync(ROOT)) {
    if (SKIP_DIRS.has(f)) continue;
    try {
      if ((f.endsWith('.html') || f.endsWith('.md')) && fs.statSync(f).isFile()) {
        for (const p of resolvePermalinks(path.join(ROOT, f))) set.add(p);
      }
    } catch {}
  }

  // _posts — strip YYYY-MM-DD- prefix, then resolve (frontmatter or filename)
  const postsDir = path.join(ROOT, '_posts');
  if (fs.existsSync(postsDir)) {
    for (const f of fs.readdirSync(postsDir)) {
      if (!f.endsWith('.md')) continue;
      const slug = f.replace(/^\d{4}-\d{2}-\d{2}-/, '').replace(/\.md$/, '');
      for (const p of resolvePermalinks(path.join(postsDir, f), slug)) set.add(p);
    }
  }

  // _series — collection pages (AISE, Agent OS, etc.) — their `permalink:`
  // frontmatter is the canonical URL of the series landing page.
  const seriesDir = path.join(ROOT, '_series');
  if (fs.existsSync(seriesDir)) {
    for (const f of fs.readdirSync(seriesDir)) {
      if (!f.endsWith('.md')) continue;
      for (const p of resolvePermalinks(path.join(seriesDir, f))) set.add(p);
    }
  }

  // _drafts (if present) — strip any date prefix same as posts
  const draftsDir = path.join(ROOT, '_drafts');
  if (fs.existsSync(draftsDir)) {
    for (const f of fs.readdirSync(draftsDir)) {
      if (!f.endsWith('.md')) continue;
      const slug = f.replace(/^\d{4}-\d{2}-\d{2}-/, '').replace(/\.md$/, '');
      for (const p of resolvePermalinks(path.join(draftsDir, f), slug)) set.add(p);
    }
  }

  // Generated by Jekyll plugins
  for (const p of ['/feed.xml', '/sitemap.xml', '/archive/', '/tags/', '/subscribe/', '/favicon.png', '/humans.txt', '/site.webmanifest', '/opensearch.xml', '/LICENSE', '/assets/images/og-default.png']) {
    set.add(p);
  }
  return set;
}

function fileExists(url) {
  const candidates = [
    path.join(ROOT, url),
    path.join(ROOT, url + '.html'),
    path.join(ROOT, url + '/index.html'),
    path.join(ROOT, url + '.png'),
    path.join(ROOT, url + '.svg'),
    path.join(ROOT, url + '.jpg'),
    path.join(ROOT, url + '.xml'),
  ];
  return candidates.some(c => fs.existsSync(c));
}

function main() {
  const htmlFiles = walk(ROOT);
  const permalinks = buildPermalinkMap();
  const linkRe = /(?:href|src)=["']([^"']+)["']/g;

  const allLinks = [];
  for (const f of htmlFiles) {
    const txt = fs.readFileSync(f, 'utf8');
    let m;
    while ((m = linkRe.exec(txt)) !== null) {
      allLinks.push({ file: f.replace(process.cwd() + '/', ''), url: m[1] });
    }
  }

  const internal = allLinks.filter(l =>
    l.url.startsWith('/') &&
    !l.url.startsWith('//') &&
    !l.url.includes('{{') &&
    !l.url.includes('{%') &&
    !l.url.includes('site.') &&
    !l.url.includes('page.')
  );

  const broken = [];
  for (const l of internal) {
    let p = l.url.split('#')[0].split('?')[0];
    if (!p) continue;
    p = p.replace(/\/+$/, '');
    if (!p) continue;
    if (!permalinks.has(p) && !permalinks.has(p + '/') && !fileExists(p)) {
      broken.push(l);
    }
  }

  console.log(`HTML files scanned: ${htmlFiles.length}`);
  console.log(`Internal links: ${internal.length}`);
  console.log(`Broken: ${broken.length}`);
  if (broken.length) {
    console.log('');
    for (const b of broken) {
      console.log(`  ${b.file} :: ${b.url}`);
    }
    process.exit(1);
  } else {
    console.log('✓ All internal links resolve');
    process.exit(0);
  }
}

main();
